function setLanguage(lang) {
  localStorage.setItem('lang', lang);
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });
}

function initLanguage() {
  const saved = localStorage.getItem('lang') || 'en';
  const select = document.getElementById('language-select');
  const toggle = document.getElementById('language-toggle');
  if (select) {
    select.value = saved;
    select.addEventListener('change', e => {
      setLanguage(e.target.value);
      select.classList.add('hidden');
    });
  }
  if (toggle && select) {
    toggle.addEventListener('click', () => {
      select.classList.toggle('hidden');
    });
  }
  setLanguage(saved);
}

function initTheme() {
  const saved = localStorage.getItem('theme');
  if (saved === 'dark') {
    document.body.classList.add('dark-theme');
  }
  const toggle = document.getElementById('theme-toggle');
  if (toggle) {
    const updateIcon = () => {
      toggle.textContent = document.body.classList.contains('dark-theme') ? 'â˜€ï¸' : 'ðŸŒ™';
    };
    updateIcon();
    toggle.addEventListener('click', () => {
      document.body.classList.toggle('dark-theme');
      localStorage.setItem('theme', document.body.classList.contains('dark-theme') ? 'dark' : 'light');
      updateIcon();
    });
  }
}

function initAnimations() {
  const observer = new IntersectionObserver(entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('show');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.1 });

  document.querySelectorAll('.animate').forEach(el => observer.observe(el));
}

function initCarousel() {
  const images = Array.from(document.querySelectorAll('.image-item img'));
  if (!images.length) return;
  const imageSets = [
    ['NEWIMAGES/76da7245bcbb34e56daa12.jpg', 'NEWIMAGES/46776ce8a2162a48730711.jpg', 'NEWIMAGES/c976cce902178a49d3068.jpg'],
    ['NEWIMAGES/c9791fe5d11b5945000a9.jpg', 'NEWIMAGES/f03bc0a40e5a8604df4b13.jpg', 'NEWIMAGES/76da7245bcbb34e56daa12.jpg']
  ];

  let index = 0;

  function showSet(i) {
    images.forEach((img, idx) => {
      img.src = imageSets[i][idx];
    });
    if (images[1]) {
      if (images[1].complete) {
        updateOverlayColor();
      } else {
        images[1].addEventListener('load', updateOverlayColor, { once: true });
      }
    }
  }

  showSet(index);

  setInterval(() => {
    images.forEach(img => img.classList.add('fade'));
    setTimeout(() => {
      index = (index + 1) % imageSets.length;
      showSet(index);
      images.forEach(img => img.classList.remove('fade'));
    }, 1000);
  }, 5000);
}

function updateOverlayColor() {
  document.documentElement.style.setProperty('--overlay-color', '#000000');
}

function initNavScroll() {
  const nav = document.querySelector('nav');
  if (!nav) return;
  const navHeight = nav.offsetHeight;
  let lastScroll = window.pageYOffset;
  window.addEventListener('scroll', () => {
    const current = window.pageYOffset;
    if (current > lastScroll) {
      nav.style.top = `-${navHeight}px`;
    } else {
      nav.style.top = '0';
    }
    lastScroll = current;
  });
}

document.addEventListener('DOMContentLoaded', () => {
  initLanguage();
  initTheme();
  initAnimations();
  initCarousel();
  initNavScroll();
});
